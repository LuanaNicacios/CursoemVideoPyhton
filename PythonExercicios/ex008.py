# Exercício 007 -- Converter metros para milímetros #

m = float(input('Dígite o valor em metros: '))
cem = m*100
mm = m*1000
print('O valor de: {} metros em: \n Centimetros é: {}cm \n Milímetros é: {}mm '.format(m, cem, mm))

